		Bedanya Tempe Mendoan dan Tempe Goreng Biasa
		Tau kan bedanya tempe mendoan dan tempe goreng biasa? Jangan bilang kamu kira itu sama, karena ternyata beda nih. Tempe goreng dan tempe mendoan memang sama-sama tempe tapi secara arti berbeda, cara masak dan cara membuat tempenya pun berbeda.

		Tempe mendoan dibuat dari kacang kedelai juga kok dengan metode yang berbeda dari tempe biasanya. Tempe mendoan disusun di dalam daun pisang dengan kacang kedelai yang dipisah agak berjarak dan tidak banyak, makanya bentuknya pasti tipis. Sedangkan tempe goreng pasti agak tebal dan kacang kedelainya lebih banyak.

		Secara rasa memang berbeda karena banyaknya kacang kedelai dan proses menggoreng dengan tingkat kematangan yang juga berbeda. Tempe mendoan jika digoreng crispy namanya bukan tempe mendoan, tempe goreng jika digoreng dan engga crispy pasti dimarahin karena belom matang.
