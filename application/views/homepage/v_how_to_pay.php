	<h5 align="center"><b>TITPIN</b> Payment Guide</h5>
	<p>
		<ol>
			<li>Anda dapat menggunakan beberapa metode pembayaran, yaitu:
				<ul xss="removed">
					<li><strong>Internet Banking:</strong> Masukkan Username dan Password Anda.</li>
					<li><strong>SMS Banking:</strong> Menyesuaikan bentuk penulisan SMS tiap bank.</li>
					<li><strong>ATM Bersama/Transfer ATM:</strong>&nbsp;Masukkan kartu ATM dan pin ATM Anda.</li>
				</ul>
			</li>
			<li>Pilih menu transfer, kemudian pilih <em><strong>transfer sesama bank</strong></em>&nbsp;atau <em><strong>transfer berbeda bank</strong></em>.</li>
			<li>Bila Anda <em><strong>transfer berbeda bank</strong></em>&nbsp;silahkan masukkan kode bank dan diikuti nomor rekening sesuai rekening tujuan Anda.
				<ul xss="removed">
					<li><strong>Bank BRI:</strong>&nbsp;<span xss="removed">002</span><span xss="removed">0060-01-018999-53-0</span></li>
					<li><span><strong>Bank Muamalat:</strong>&nbsp;<span xss="removed">147</span>5370004532</span></li>
				</ul>
			</li>
			<li><span xss="removed">Bila Anda <em><strong>transfer sesama bank</strong></em> silahkan masukkan nomor rekening sesuai rekening tujuan Anda.</span>
				<ul xss="removed">
					<li><strong>Bank BRI:&nbsp;</strong><span>0060-01-018999-53-0</span></li>
					<li><strong>Bank Muamalat:</strong>&nbsp;5370004532</li>
				</ul>
			</li>
			<li>Kemudian masukkan jumlah transfer sesuai dengan <strong>Invoice</strong> Anda. pastikan Anda memasukan jumlah transfer sesuai dengan total bayar Anda <span xss="removed">(hingga 3 digit terakhir)</span>.</li>
			<li>Pastikan nama pemilik rekening dan jumlah transfer sesuai, agar menghindari Anda dari kesalahan transfer.<br>
				<ul xss="removed">
					<li><strong>Bank BRI:</strong> Willy Wahyu</li>
					<li><strong>Bank Muamalat:</strong> Willy Wahyu</li>
				</ul>
			</li>
			<li>Jika Anda sudah memastikan jumlah transfer dan nama pemilik rekening sesuai, Anda dapat menekan tombol selanjutnya/selesai.</li>
			<li>Selesai, pembayaran Anda akan kami proses dalam waktu maksimal <strong>1 x 24 jam</strong>.</li>
		</ol>
